<h4>PRIMERA</h4>
<P>II.- CURSOS DE FORMACIÓN Y PERFECIONAMIENTO SUPERADOS (máximo 3 puntos)
<P>1. No se valorarán los cursos en los que no conste de manera expresa el número de horas o de créditos de duración de los mismos. De
    no constar otra cosa cuando los cursos vinieran expresados en créditos se entenderá que cada crédito equivale a 10 horas. En el caso
    de que existiera discordancia entre los créditos realizados y las horas certificadas, siempre serán consideradas las horas y se seguirá la
    proporción establecida de 1 crédito igual a 10 horas.
<P>2. Únicamente se sumarán las horas de todos los cursos que consten de 10 o más horas, no puntuándose ni pudiendo ser acumulativos
    el resto del número de horas inferiores a 10.
<P>3. Las certificaciones de las actividades de formación permanente se valorarán de acuerdo con la normativa aplicable a dicha actividad
    en cada momento dado la posible existencia de diferencias en el tratamiento de las mismas.
    En la presente convocatoria las disposiciones aplicables son las siguientes:<br>
     Orden EDU/1057/2014, de 4 de diciembre, por la que se regulan las modalidades, convocatoria, reconocimiento, certificación y
    registro de las actividades de formación permanente del profesorado de enseñanzas no universitarias que presta sus servicios
    en centros docentes sostenidos con fondos públicos en la Comunidad de Castilla y León organizadas por la Red de Formación y
    se establecen las condiciones de reconocimiento de las actividades de formación organizadas por otras entidades (BOCYL 16 de
    diciembre, entrada en vigor el 17 de diciembre), teniendo presente la modificación operada por la Orden EDU/1020/2016, de
    30 de noviembre, BOCYL de 12 de diciembre y entrada en vigor el 12 de febrero de 2017).<br>
     Orden EDU/2886/2011, de 20 de octubre, por la que se regula la convocatoria, reconocimiento, certificación y registro de las
    actividades de formación permanente del profesorado (BOE 28 de octubre y entrada en vigor el 29 de octubre).<br>
     Orden ministerial de 26 de noviembre de 1992, por la que se regula la convocatoria, reconocimiento, certificación y registro de
    las actividades de formación permanente del profesorado y se establece la equivalencia de las actividades de investigación y de
    las titulaciones universitarias (BOE del 10 de diciembre) y las Resoluciones de 27 de abril de 1994 (BOE de 25 de mayo), de 24 de
    enero de 1996 (BOE de 13 de febrero), de 12 de noviembre de 1998 (BOE de 8 de diciembre), y de 8 de octubre de 2002 (BOE
    del 23) que la desarrollan.
<P>4. Respecto a la realización de varias actividades de formación a distancia en el mismo periodo de tiempo, se tendrá en cuenta:<br>
     Si la actividad se ha desarrollado conforme a la Orden de 26 de noviembre de 20019, es decir, cuando la actividad se haya
    iniciado antes de 29 de octubre de 2011, únicamente se reconocerá una de las actividades.<br>
     Si la actividad se ha realizado conforme a la Orden EDU/2886/2011, de 20 de octubre, es decir, actividad iniciada a partir
    del 29 de octubre de 2011, o la Orden EDU/1057/2014, de 4 de diciembre, esto es, actividad iniciada a partir del 17 de
    diciembre, no será aplicable esta limitación.
<p>5. Para la valoración de las actividades de formación a distancia, deberán tenerse en cuenta los límites de horas de formación
    recogidos en la normativa aplicable:<br>
     El tiempo de dedicación semanal, con carácter general, no podrá ser superior a 10 horas. Las actividades que superen
    esta dedicación, deberán justificar la propuesta. (artículo 10 d) de la Orden EDU/2886/2011).<br>
     Si un asistente participa simultáneamente en varias actividades a distancia y la coincidencia en el tiempo de las mismas
    es superior a 25 horas semanales, solo se le podrá reconocer una de las actividades, debiendo indicar el asistente la
    actividad formativa a distancia que prefiere que sea reconocida, certificada e inscrita en el correspondiente Rescrito
    (Artículo 9.2 d) de la Orden EDU/1057/2014, de 4 de diciembre, introducido por la Orden EDU/1020/2016, de 30 de
    noviembre, y aplicable desde el 12 de febrero de 2017).<br>
     El número máximo de horas de formación a asignar a estas actividades será de 100 y el mínimo de 20. Excepcionalmente,
    la dirección general competente en materia de formación de profesorado podrá autorizar la impartición de actividades
    formativas que superen en su duración lo anteriormente manifestado (Artículo 9.2 b) de la Orden EDU/1057/2014, de 4
    de diciembre).<br>
     La distribución temporal de la actividad habrá de tener en cuenta las horas totales del mismo, marcándose una división
    temporal para cada uno de los módulos. En todo caso, el número de horas dedicadas a la realización de la actividad no
    deberá ser superior a 20 horas semanales, salvo autorización expresa (Artículo 9.2 c) de la Orden EDU/1057/2014, de 4
    de diciembre).
<p>6. En ningún caso serán valorados aquellos cursos, incluidos los de doctorado, o asignaturas integrantes de un Título académico, Máster
    u otra Titulación de Postgrado. Tampoco se valorarán los proyectos desarrollados durante la carrera universitaria o que formen parte
    del expediente académico. Asimismo, teniendo en cuenta el criterio establecido para la valoración del subapartado 3.1.1, no procederá
    valorar el certificado de aptitud pedagógica o el título de especialización didáctica en este subapartado a ningún participante, a
    excepción del Cuerpo de profesores de música y artes escénicas, por no ser un requisito de ingreso. Tampoco se valorarán los proyectos
    desarrollados durante la carrera universitaria o que formen parte del expediente académico.
