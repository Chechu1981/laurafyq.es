<h3 style="text-align: center">DISPOSICIONES ADICIONALES</h3>
<h4>GENÉRICA</h4>
<P>1. Los aspirantes no podrán alcanzar más de 10 puntos por la valoración de sus méritos.</P>
<P>2. Para la justificación de los méritos se admitirán documentos originales o copias, teniendo en cuenta lo indicado en el apartado tercero.1.</p>
<P>3. Toda documentación acreditativa de los méritos deberá presentarse en castellano. De conformidad con lo dispuesto en el artículo 15.2 de
    la Ley 39/2015, de 1 de octubre, del Procedimiento Administrativo Común de las Administraciones Públicas, deberán traducirse al castellano
    los documentos que, redactados en lengua oficial de una Comunidad Autónoma, deban surtir efectos fuera del territorio de esa Comunidad.
    Asimismo, la documentación redactada en lengua extranjera deberá presentarse acompañada de su traducción jurada oficial al castellano,
    sin perjuicio de que por la Administración puedan recabarse informes complementarios para verificar la autenticidad de los documentos
    presentados.
<P>4. Los méritos acreditados por los participantes han de tenerse cumplidos o reconocidos en la fecha de finalización del plazo de presentación
    de solicitudes establecido en el apartado tercero.6. Únicamente se valorarán, por tanto, los méritos perfeccionados y acreditados hasta la
    finalización del mismo.
