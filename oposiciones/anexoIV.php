<p>9. Las actividades organizadas o impartidas por universidades (públicas o privadas) y sus títulos no oficiales (títulos propios) previstos en
    la disposición adicional undécima del Real Decreto 1393/2007, de 29 de octubre, por el que se establece la ordenación de las
    enseñanzas universitarias oficiales, se podrán baremar por este apartado II si reúnen los requisitos generales.
<p>10. En todo caso, en la certificación deberá constar el sello de la universidad y la firma de la autoridad académica competente de la misma
    conforme disponga la normativa reguladora de las actividades de formación permanente del profesorado.
