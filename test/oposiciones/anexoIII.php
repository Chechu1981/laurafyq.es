<h3 style="text-align: center">DISPOSICIONES COMPLEMENTARIAS</h3>
<h4>GENÉRICA</h4>
<ol>
    <li>Los aspirantes no podrán alcanzar más de 10 puntos por la valoración de sus méritos.</li>
    <li>Para la justificación de los méritos se admitirán documentos originales o copias, teniendo en cuenta lo indicado en el apartado
        tercero.1.</li>
    <li>Toda documentación acreditativa de los méritos deberá presentarse en castellano. De conformidad con lo dispuesto en el artículo
        15.2 de la Ley 39/2015, de 1 de octubre, del Procedimiento Administrativo Común de las Administraciones Públicas, deberán
        traducirse al castellano los documentos que, redactados en lengua oficial de una Comunidad Autónoma, deban surtir efectos fuera
        del territorio de esa Comunidad.<p></p>Asimismo, la documentación redactada en lengua extranjera deberá presentarse acompañada de su traducción jurada oficial al
        castellano, sin perjuicio de que por la Administración puedan recabarse informes complementarios para verificar la autenticidad
        de los documentos presentados.</li>
    <li>Los méritos acreditados por los participantes han de tenerse cumplidos o reconocidos en la fecha de finalización del plazo de
        presentación de solicitudes establecido en el apartado tercero.6. Únicamente se valorarán, por tanto, los méritos perfeccionados y
        acreditados hasta la finalización del mismo.<p></p>
        No serán tenidos en cuenta los méritos no alegados, ni tampoco aquellos que no se justifiquen documentalmente durante el plazo
        de presentación de los mismos, sin perjuicio de lo establecido en el apartado decimosexto de la convocatoria relativo a la
        subsanación.</li>
    <li>Se entenderá por Administración educativa el Ministerio y las Consejerías de las Comunidades Autónomas que tengan atribuida las
        competencias en materia educativa.</li>
</ol>
<h4 id="i">PRIMERA</h4>
<p>I.- EXPERIENCIA DOCENTE PREVIA (máximo 7 puntos)</p>
<ol>
    <li>Únicamente se tendrá en cuenta un máximo de diez años, cada uno de los cuales deberá ser valorado en uno solo de los
        subapartados anteriores.</li>  
    <li>Serán tenidos en cuenta únicamente los certificados de servicios en los que conste las fechas exactas de comienzo y terminación
        de los mismos o que se indique que continúa prestando servicios a la fecha de finalización el plazo de presentación de
        solicitudes</li>
    <li>La valoración de la experiencia docente, no se verá reducida, aun cuando se hayan prestado servicios inferiores a la jornada
        completa</li>
    <li>Un mismo mérito no podrá valorarse simultáneamente por más de un apartado o subapartado. No podrán acumularse las
        fracciones de años prestadas en distintos cuerpos y/o especialidades a efectos de generar meses completos. </li>
    <li>La experiencia docente se entenderá prestada en centros públicos cuando el profesorado haya sido nombrado directamente
        por las Administraciones educativas, aportando la documentación emitida por el correspondiente órgano de las distintas
        Administraciones educativas que tengan atribuidas las competencias en materia de personal docente.</li>
    <li>La experiencia en centros que posteriormente hayan sido transferidos al Ministerio o las Consejerías de las Comunidades
        Autónomas competentes en materia educativa, solo se valorará como centros públicos desde la fecha de efectos de dicha
        transferencia.</li>
    <li>No podrán acumularse las puntuaciones cuando los servicios se hayan prestado simultáneamente en más de un centro docente
        o se hayan desempeñado en un mismo centro distintas especialidades al mismo tiempo. En caso de prestar servicios en un
        mismo centro en distintas especialidades, la experiencia se computará a la especialidad con mayor carga lectiva, a menos que
        el aspirante indique expresamente su deseo de imputarlo a una especialidad determinada. </li>
    <li>La experiencia docente que, en su caso, se acredite por impartir enseñanzas de religión en centros públicos, será computada
        dentro del apartado 1.2.</li>
    <li>Los servicios docentes prestados en Universidades públicas o privadas serán baremables por el 1.2 o el 1.4, respectivamente.</li>
    <li>En el caso de centros que no estén actualmente en funcionamiento, en defecto del certificado del Director con el visto bueno
        del Área de Inspección Educativa, la experiencia podrá justificarse mediante certificado expedido por dicha Área, de
        conformidad con los datos que existan en la misma.</li>
    <li>Los servicios prestados en el extranjero se acreditarán mediante certificados expedidos por las Administraciones Educativas
        correspondientes de los respectivos países, en los que deberán constar las fechas de inicio y fin de la prestación de servicios y
        el carácter de centro público o privado, el nivel educativo y la especialidad impartida. </li>
    <li>Cuando no se acrediten suficientemente el cuerpo o nivel educativo los servicios se entenderán prestados en distinto cuerpo o
        distinto nivel o etapa educativa, respectivamente.
    </li>
    <li>Se entiende por centros públicos los centros a los que se refiere el Capítulo II del Título IV de la Ley Orgánica 2/2006, de 3 de
        mayo, de Educación, integrados en la red pública de centros creados y sostenidos por las Administraciones educativas.</li>
    <li>Un mismo mérito no podrá valorarse simultáneamente por más de un apartado o subapartado. En estos casos, será valorado
        el mérito más ventajoso o beneficioso para el aspirante.</li>


</ol>

<h4 id="ii">SEGUNDA</h4>
<p>II.- FORMACIÓN ACADÉMICA (máximo 5 puntos)</p>
<ol>

    <li>A los efectos de su valoración por el <span style="color: red">apartado II</span>, únicamente se tendrán en cuenta los títulos con validez oficial en el Estado
        Españo</li>
    <li>En defecto del correspondiente título podrá presentarse certificación del abono de los derechos de su expedición conforme a
        la Orden de 8 de julio de 1988, para la aplicación de los Reales Decretos 185/1985, de 23 de enero, y 1496/1987, de 6 de
        noviembre, en materia de expedición de títulos universitarios oficiales (B.O.E. del 13), o certificado supletorio de la titulación
        expedidos de acuerdo con lo previsto, en su caso, en la Orden ECI/2514/2007, de 13 de agosto, sobre expedición de títulos
        universitarios oficiales de Máster y Doctor (B.O.E. del 21) o en el Real Decreto 1002/2010, de 5 de agosto, sobre expedición de
        títulos universitarios oficiales (B.O.E. de 6 de agosto).</li>
    <li>En caso de que las titulaciones se hayan obtenido en el extranjero, deberá haberse concedido la correspondiente homologación
        de conformidad con lo dispuesto en los Reales Decretos 86/1987, de 16 de enero, 285/2004, de 20 de febrero, o 967/2014, de 
        21 de noviembre, o su reconocimiento al amparo de lo establecido por la Directiva 2005/36/CEE o Directiva 2013/55/UE y el
        Real Decreto 1837/2008, de 8 de noviembre, o el Real Decreto 581/2017, de 9 de junio. </li>
    <li>Asimismo, en el caso de titulaciones obtenidas en el extranjero y con objeto de calcular la nota media del expediente
        académico, deberá aportar certificación expedida por la Administración educativa del país en el que se obtuvo el título que
        indique la nota media deducida de las calificaciones obtenidas en toda la carrera y exprese además la calificación máxima y
        mínima obtenibles de acuerdo con el sistema académico correspondiente, a efectos de determinar su equivalencia con las
        calificaciones españolas</li>
    <li>En el caso de que no se aporte la certificación académica personal, pero se presente el título o la certificación de abono de
        derechos de expedición de éste, o certificación supletoria provisional, se considerará que el aspirante obtuvo la nota media de
        aprobado</li>
    <li>Para la obtención de la nota media del expediente académico en los casos en que no figure la expresión numérica concreta se
        aplicarán las siguientes equivalencias:<p></p>
        <table style="margin: auto">
            <tr>
                <th style="padding-right: 180px">Notas expresadas en valores de 0 a 10</th>
                <th>Notas expresadas en valores de 0 a 4</th>
            </tr>
            <tr>
                <td>Aprobado: Cinco puntos.</td>
                <td>Aprobado: Un punto.</td>
            </tr>
            <tr>
                <td>Notable: Siete puntos. </td>
                <td>Notable: Dos puntos. </td>


            </tr>
            <tr>
                <td>Sobresaliente: Nueve puntos. </td>
                <td>Sobresaliente: Tres puntos.</td>

            </tr>
            <tr>
                <td>Matrícula de honor: Diez puntos.</td>
                <td>Matrícula de honor: Cuatro puntos.</td>
            </tr>
        </table>
        <p></p>
        Aquellas calificaciones que contengan la expresión literal “bien” se considerarán equivalentes a seis puntos y las de “apto” <span style="color: red">a 5
            puntos.</span> En el caso de las materias “convalidadas” o incorporadas al expediente por reconocimiento de créditos se considerarán
        equivalentes a cinco puntos, salvo que se aporte certificación en la que se acredite la calificación que dio origen a la
        convalidación o el reconocimiento, considerándose en este caso la calificación originaria.
    </li>
    <li>Para la obtención de la nota media del expediente académico, cuando los estudios se hayan cursado por créditos, se utilizará
        la siguiente tabla de equivalencias:<p></p>
        Aprobado: Un punto.<br>
        Notable: Dos puntos.<br>
        Sobresaliente: Tres puntos.<br>
        Matricula de honor: Cuatro puntos.<p></p>
        La nota media en estos casos se calculará de la siguiente forma: suma de los créditos obtenidos por el alumno multiplicados
        cada uno de ellos por el valor de las calificaciones que correspondan, y dividida por el número de créditos totales obtenidos
        por el alumno</li>
    <li>En ningún caso se tomarán en consideración para obtener la nota media del expediente académico las calificaciones
        correspondientes a materias complementarias, proyectos de fin de carrera, tesinas o análogos. </li>
    <li>En el apartado 2.1 se valorará el expediente académico del título alegado por los aspirantes que ha sido declarado equivalente
        a efectos de docencia y permita el ingreso a la función pública docente.</li>
    <li>En el apartado 2.2.1 solo se tendrá en cuenta un título Máster. Para la valoración de este apartado debe indicarse que los
        Másteres oficiales solo existen a partir del año 2005 (Real Decreto 56/2005, de 21 de enero, por el que se regulan los estudios
        universitarios oficiales de postgrado) no debiéndose confundir con los títulos propios no oficiales de las universidades que hasta
        la entrada en vigor del Real Decreto 1393/2007, de 29 de octubre, por el que se establece la ordenación de las enseñanzas
        universitarios oficiales, hayan podido incluir en su denominación la palabra “Máster”. Estos títulos propios podrían ser
        valorados por el apartado 3.1 siempre que cumplan el resto de requisitos exigidos.<p></p>
        En ningún caso debe baremarse por el apartado 2.2.1 el título oficial de Máster exigido como requisito para el ingreso en la
        función pública docente, a excepción del Cuerpo de profesores de música y artes escénicas, por no ser este un requisito de
        ingreso para este cuerpo.<p></p>
        Asimismo, cuando se alegue el título de Doctor no se valorará el título de Máster oficial que constituya un requisito de acceso
        al doctorado.<p></p>
        En el apartado 2.2.1.solo se tendrá en cuenta un certificado-diploma y será incompatible con el apartado 2.2.2.
        Los cursos de doctorado no son suficiencia investigadora, debiendo ser está reconocida explícitamente.<p></p>
        Los cursos de postgrado, sea cual sea su duración, no pueden valorarse en el 2.2.1, pudiendo únicamente valorarse dentro del
        apartado 3.1 siempre que cumplan el resto de requisitos exigidos. </li>
    <li>Para poder obtener puntuación en el apartado 2.3 “Otras titulaciones de carácter oficial” deberá presentarse fotocopia de
        cuantos títulos se posean, además del título alegado para el ingreso en el cuerpo desde el que participa. 
        <p></p>
        Para la valoración de estos apartados será necesario aportar certificación académica de todos los títulos o ciclos que se posean,
        en donde conste de forma expresa que se han cursado y superado todas las asignaturas o créditos conducentes para la
        obtención de dichos títulos o ciclos. Con carácter general, la presentación de la certificación académica tiene por objeto conocer
        si un título operó como primer ciclo a los efectos de obtener una titulación de segundo ciclo, o el porcentaje de créditos
        reconocidos en el caso de Títulos de Grado.<p></p>
        Se entenderá asimismo por titulación académica de primer ciclo el título de diplomado o bien haber superado los tres primeros
        cursos de una Licenciatura, Ingeniería o Arquitectura.<p></p>
        No serán baremables los cursos de adaptación a Grado a efectos de titulaciones de primer ciclo o diplomatura.
        No se valorarán los primeros ciclos que hayan permitido la obtención de otras titulaciones académicas de ciclo largo que se
        aleguen como méritos.<p></p>
        Para la valoración del apartado 2.3.1 y 2.3.2 no se considerarán como títulos distintos, las diferentes menciones que se asienten
        en una misma titulación.<p></p>
        En las titulaciones correspondientes al segundo ciclo que presenten los funcionarios de carrera de los cuerpos de Profesores
        de Enseñanza Secundaria solo se podrán valorar a partir del segundo título de esta naturaleza, independientemente de que
        para su ingreso en el cuerpo lo hicieran por una diplomatura por estar expresamente declarada equivalente a efectos de
        docencia, dado que, con carácter general, el requisito para el ingreso en el cuerpo es una titulación superior.<p></p>
        La presentación del título de Licenciado, Ingeniero o Arquitecto dará lugar exclusivamente al reconocimiento de la titulación
        de segundo ciclo.<p></p>
        Las titulaciones de sólo segundo ciclo y los títulos declarados equivalentes a todos los efectos al título universitario de
        licenciado, únicamente se valorarán como un segundo ciclo.</li>
</ol>
<h4 id="iii">TERCERA</h4>
<p>III.- OTROS MÉRITOS (máximo 2 puntos)
</p>
<ol>
    <li>No se valorarán los cursos en los que no conste de manera expresa el número de horas o de créditos de duración de los mismos.
        De no constar otra cosa cuando los cursos vinieran expresados en créditos se entenderá que cada crédito equivale a 10 horas.
        En el caso de que existiera discordancia entre los créditos realizados y las horas certificadas, siempre serán consideradas las
        horas y se seguirá la proporción establecida de 1 crédito igual a 10 horas.
    </li>
    <li>Únicamente se sumarán las horas de todos los cursos que consten de 10 o más horas, no puntuándose ni pudiendo ser
        acumulativos el resto del número de horas inferiores a 10.
    </li>
    <li>Las certificaciones de las actividades de formación permanente se valorarán de acuerdo con la normativa aplicable a dicha
        actividad en cada momento dado la posible existencia de diferencias en el tratamiento de las mismas.
        En la presente convocatoria las disposiciones aplicables son las siguientes:
        <ul>
            <li>Orden EDU/1057/2014, de 4 de diciembre, por la que se regulan las modalidades, convocatoria, reconocimiento,
                certificación y registro de las actividades de formación permanente del profesorado de enseñanzas no universitarias que
                presta sus servicios en centros docentes sostenidos con fondos públicos en la Comunidad de Castilla y León organizadas
                por la Red de Formación y se establecen las condiciones de reconocimiento de las actividades de formación organizadas
                por otras entidades (BOCYL 16 de diciembre, entrada en vigor el 17 de diciembre), teniendo presente la modificación
                operada por la Orden EDU/1020/2016, de 30 de noviembre, BOCYL de 12 de diciembre y entrada en vigor el 12 de febrero
                de 2017).</li>
            <li>Orden EDU/2886/2011, de 20 de octubre, por la que se regula la convocatoria, reconocimiento, certificación y registro
                de las actividades de formación permanente del profesorado (BOE 28 de octubre y entrada en vigor el 29 de octubre).</li>
            <li>Orden ministerial de 26 de noviembre de 1992, por la que se regula la convocatoria, reconocimiento, certificación y
                registro de las actividades de formación permanente del profesorado y se establece la equivalencia de las actividades de
                investigación y de las titulaciones universitarias (BOE del 10 de diciembre) y las Resoluciones de 27 de abril de 1994 (BOE
                de 25 de mayo), de 24 de enero de 1996 (BOE de 13 de febrero), de 12 de noviembre de 1998 (BOE de 8 de diciembre),
                y de 8 de octubre de 2002 (BOE del 23) que la desarrollan.</li>
        </ul>
    </li>
    <li>Respecto a la realización de varias actividades de formación a distancia en el mismo periodo de tiempo, se tendrá en cuenta:
        <ul>
            <li>Si la actividad se ha desarrollado conforme a la Orden de 26 de noviembre de 20019, es decir, cuando la actividad se haya
                iniciado antes de 29 de octubre de 2011, únicamente se reconocerá una de las actividades. </li>
            <li>Si la actividad se ha realizado conforme a la Orden EDU/2886/2011, de 20 de octubre, es decir, actividad iniciada a partir
                del 29 de octubre de 2011, o la Orden EDU/1057/2014, de 4 de diciembre, esto es, actividad iniciada a partir del 17 de
                diciembre, no será aplicable esta limitación.</li>
        </ul>
    </li>
    <li>Para la valoración de las actividades de formación a distancia, deberán tenerse en cuenta los límites de horas de formación
        recogidos en la normativa aplicable:
        <ul>
            <li>El tiempo de dedicación semanal, con carácter general, no podrá ser superior a 10 horas. Las actividades que superen
                esta dedicación, deberán justificar la propuesta. (artículo 10 d) de la Orden EDU/2886/2011).</li>
            <li>Si un asistente participa simultáneamente en varias actividades a distancia y la coincidencia en el tiempo de las mismas
                es superior a 25 horas semanales, solo se le podrá reconocer una de las actividades, debiendo indicar el asistente la
                actividad formativa a distancia que prefiere que sea reconocida, certificada e inscrita en el correspondiente Rescrito
                (Artículo 9.2 d) de la Orden EDU/1057/2014, de 4 de diciembre, introducido por la Orden EDU/1020/2016, de 30 de
                noviembre, y aplicable desde el 12 de febrero de 2017).</li>
            <li>El número máximo de horas de formación a asignar a estas actividades será de 100 y el mínimo de 20. Excepcionalmente,
                la dirección general competente en materia de formación de profesorado podrá autorizar la impartición de actividades
                formativas que superen en su duración lo anteriormente manifestado (Artículo 9.2 b) de la Orden EDU/1057/2014, de 4
                de diciembre).</li>
            <li>La distribución temporal de la actividad habrá de tener en cuenta las horas totales del mismo, marcándose una división
                temporal para cada uno de los módulos. En todo caso, el número de horas dedicadas a la realización de la actividad no
                deberá ser superior a 20 horas semanales, salvo autorización expresa (Artículo 9.2 c) de la Orden EDU/1057/2014, de 4
                de diciembre).</li>
        </ul> 
    </li>
    <li>En ningún caso serán valorados aquellos cursos, incluidos los de doctorado, o asignaturas integrantes de un Título académico,
        Máster u otra Titulación de Postgrado. Tampoco se valorarán los proyectos desarrollados durante la carrera universitaria o que
        formen parte del expediente académico. Asimismo, teniendo en cuenta el criterio establecido para la valoración del
        subapartado 2.2.1, no procederá valorar el certificado de aptitud pedagógica o el título de especialización didáctica en este
        subapartado a ningún participante, a excepción del Cuerpo de profesores de música y artes escénicas, por no ser un requisito
        de ingreso. Tampoco se valorarán los proyectos desarrollados durante la carrera universitaria o que formen parte del
        expediente académico.</li>
    <li>No se valorarán las actividades organizadas por instituciones tales como colegios profesionales, centros privados de enseñanza,
        colegios de doctores y licenciados, asociaciones culturales y/o vecinales, universidades populares, patronatos municipales, etc,
        salvo que cuenten con la correspondiente homologación de la Administración educativa.</li>
    <li>No obstante, sí serán baremadas las actividades organizadas por Instituciones sin ánimo de lucro cuando la certificación de las
        mismas expedida por el órgano convocante acredite fehacientemente el reconocimiento de homologación o que se realiza en
        convenio con las Administraciones educativas, o se hayan hecho en colaboración con alguna universidad y aparezca sellado y
        firmado por un cargo de la misma, debiendo figurar el sello de inscripción en el Registro General de Formación Permanente del
        Profesorado, en aquellas actividades que deban inscribirse.
    </li>
    <li>Las actividades organizadas o impartidas por universidades (públicas o privadas) y sus títulos no oficiales (títulos propios)
        previstos en la disposición adicional undécima del Real Decreto 1393/2007, de 29 de octubre, por el que se establece la
        ordenación de las enseñanzas universitarias oficiales, se podrán baremar por los apartados 3.1 y 3.2 si reúnen los requisitos
        generales del apartado correspondiente.
    </li>
    <li>En todo caso, en la certificación deberá constar el sello de la universidad y la firma de la autoridad académica competente de
        la misma conforme disponga la normativa reguladora de las actividades de formación permanente del profesorado.
    </li>
    <li>En el apartado 3.3 se tendrá en cuenta el número de autores. Asimismo, en el caso de publicaciones que solamente se dan en
        formato electrónico, se presentará un informe en el cual, el organismo emisor, certificará que la publicación aparece en la
        correspondiente base de datos bibliográfica. En este documento se indicará la base de datos, el título de la publicación, los
        autores, el año y la URL. Además se presentará, en su caso, un ejemplar impreso.</li>
    <li>En el apartado 3.3 no se baremarán publicaciones que constituyan programaciones, temarios de oposiciones, unidades
        didácticas, experiencias de clase, trabajo de asignaturas de carrera, legislación, las reiteraciones de trabajos previos, estudios
        descriptivos y enumerativos, así como ediciones de centros docentes, publicaciones aparecidas en la prensa diaria, ni prólogos
        ni artículos de opinión.<p></p>
        No obstante, dado que un libro escolar no es una programación didáctica sino que desarrolla un currículo concreto de una
        Administración educativa en el que se articulan aspectos científicos, didácticos y metodológicos, puede ser valorado dentro del
        <span style="color: red">subapartado 3.2.</span> siempre que cumplan con el resto de exigencias indicadas en el mismo.<p></p>
        En el caso de publicaciones que solamente se dan en formato electrónico, se presentará un informe en el cual, el organismo
        emisor, certificará que la publicación aparece en la correspondiente base de datos bibliográfica. En este documento se indicará
        la base de datos, el título de la publicación, los autores, el año y la URL. Además se presentará, en su caso, un ejemplar impreso.
        <p></p>
        Se excluyen las ediciones de asociaciones, agrupaciones y/o corporaciones, públicas o privadas, ya sean de padres, de vecinos,
        culturales, gremiales, de divulgación turística, etc.<p></p>
        No se valorarán los trabajos, sea cual sea su formato, destinados a la divulgación de las actividades realizadas por centros
        docentes, públicos o privados.<p></p>
        Las guías didácticas, catálogos o similares tendrán la consideración de artículos aunque se presenten en formato de libro.
        No serán valoradas las coordinaciones o ediciones de libros, revistas...etc.</li>
    <li>En el apartado 3.7 no se valorarán los certificados de Escuela Oficial de Idiomas o títulos acreditativos del conocimiento de
        idiomas que hayan sido alegados como mérito en cualquier apartado de este baremo ni como requisito de acceso al
        procedimiento.<p></p>
        Cuando el interesado solicite la baremación de títulos o certificaciones de los idiomas alemán, inglés, francés, italiano, y
        portugués, sólo se tendrán en cuenta aquellos previstos en el anexo XII Titulaciones para la acreditación de la competencia
        lingüística.<p></p>
        Cuando el interesado presente varios títulos o certificaciones del mismo idioma para su valoración como mérito, sólo se
        considerará uno, y siempre el de nivel superior.</li>
</ol>
